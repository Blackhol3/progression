BEGIN TRANSACTION;

DROP TABLE IF EXISTS competence;
CREATE TABLE competence (
	id INTEGER NOT NULL PRIMARY KEY,
	idParent INTEGER DEFAULT NULL,
	name TEXT,
	
	FOREIGN KEY (idParent) REFERENCES competence (id)
