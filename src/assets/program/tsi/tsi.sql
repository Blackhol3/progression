DROP TABLE IF EXISTS knowledge;
CREATE TABLE knowledge (
	id INTEGER NOT NULL PRIMARY KEY,
	idParent INTEGER DEFAULT NULL,
	name TEXT NOT NULL,
	semester INTEGER,
	
	FOREIGN KEY (idParent) REFERENCES knowledge (id)
);

DROP TABLE IF EXISTS knowHow;
CREATE TABLE knowHow (
	id INTEGER NOT NULL PRIMARY KEY,
	idCompetence INTEGER NOT NULL,
	name TEXT NOT NULL,
	
	FOREIGN KEY (idCompetence) REFERENCES competence (id)
);

DROP TABLE IF EXISTS knowledgeByCompetence;
CREATE TABLE knowledgeByCompetence (
	idCompetence INTEGER NOT NULL,
	idKnowledge INTEGER NOT NULL,
	
	FOREIGN KEY (idCompetence) REFERENCES competence (id),
	FOREIGN KEY (idKnowledge) REFERENCES knowledge (id)
);

